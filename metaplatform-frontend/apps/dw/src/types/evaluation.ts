// ============ Evaluation Types (V12-09 效果评估自动化) ============

import type { EvaluationReport } from '@/api/evaluations';

/**
 * 评估维度
 * - accuracy: 准确性（事实/数据是否正确）
 * - helpfulness: 有用性（是否真正解决用户问题）
 * - compliance: 合规性（是否符合安全/政策约束）
 * - efficiency: 效率（步骤是否冗余、耗时是否合理）
 * - toolUsage: 工具使用（工具调用是否恰当、参数是否正确）
 * - contextCoherence: 上下文一致性（多轮是否保持一致、是否遗忘前提）
 */
export type EvaluationDimension =
  | 'accuracy'
  | 'helpfulness'
  | 'compliance'
  | 'efficiency'
  | 'toolUsage'
  | 'contextCoherence';

/**
 * 评估模式
 * - manual: 纯人工评分
 * - llm: 纯 LLM 自动评分
 * - hybrid: 人工 + LLM 混合
 */
export type EvaluatorMode = 'manual' | 'llm' | 'hybrid';

/**
 * 维度评分
 */
export interface DimensionScore {
  dimension: EvaluationDimension;
  /** 0-100 */
  score: number;
  /** 默认 1.0 */
  weight?: number;
  /** LLM 给出的评分理由 */
  reasoning?: string;
  /** 证据片段（引用对话内容） */
  evidence?: string[];
}

/**
 * 自动评分结果
 */
export interface AutoScoreResult {
  conversationId: string;
  /** 加权总分 0-100 */
  overallScore: number;
  dimensions: DimensionScore[];
  /** 使用的 LLM 模型 */
  evaluatorModel: string;
  evaluatedAt: string;
  /** 整体评语 */
  summary: string;
  mode: EvaluatorMode;
}

/**
 * 优化建议类别
 * - prompt: 提示词优化
 * - tool: 工具配置优化
 * - knowledge: 知识库优化
 * - parameter: 模型参数优化
 * - workflow: 工作流编排优化
 */
export type SuggestionCategory = 'prompt' | 'tool' | 'knowledge' | 'parameter' | 'workflow';

/**
 * 优化建议优先级
 */
export type SuggestionPriority = 'high' | 'medium' | 'low';

/**
 * 优化建议
 */
export interface OptimizationSuggestion {
  id: string;
  category: SuggestionCategory;
  priority: SuggestionPriority;
  title: string;
  description: string;
  /** 具体改进行动 */
  action: string;
  /** 预期影响 */
  expectedImpact: string;
  /** 关联对话片段 */
  relatedEvidence?: string[];
}

/**
 * 生成优化建议请求
 */
export interface GenerateSuggestionsRequest {
  employeeId: string;
  /** 如 '7d' / '30d' / '90d' */
  period?: string;
  reportId?: string;
}

/**
 * 生成优化建议响应
 */
export interface GenerateSuggestionsResponse {
  suggestions: OptimizationSuggestion[];
  generatedAt: string;
  basedOnReportId?: string;
}

/**
 * 评估报告详情（扩展基础报告）
 */
export interface EvaluationReportDetail extends EvaluationReport {
  /** 维度评分聚合 */
  dimensions?: DimensionScore[];
  /** 优化建议 */
  suggestions?: OptimizationSuggestion[];
  autoGenerated?: boolean;
  scoreBreakdown?: Record<EvaluationDimension, number>;
  /** 与历史/基线对比 */
  comparisonBaseline?: {
    previousScore: number;
    delta: number;
  };
}

/**
 * 评分规则
 */
export interface ScoringRubric {
  id: string;
  name: string;
  dimensions: Array<{
    dimension: EvaluationDimension;
    weight: number;
    description: string;
  }>;
  updatedAt: string;
}

