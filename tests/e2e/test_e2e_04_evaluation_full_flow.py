"""链路4：效果评估全流程 E2E 测试（V11-11）。

覆盖业务流程：
  保存对话 → 自动评分 → 生成报告 → 查看详情 → 生成建议

依赖服务：TECH-AGENT（Python，真实 ASGI）
对应 V11-04 已实现端点。
"""

from __future__ import annotations

EVAL_BASE = "/api/v1/agent/evaluations"


def _conv_body(conv_id: str, employee_id: str = "emp-e2e-04") -> dict:
    return {
        "conversationId": conv_id,
        "employeeId": employee_id,
        "taskId": "task-e2e-04",
        "messages": [
            {
                "id": "msg-1",
                "role": "user",
                "content": "我的订单 #20260720-0001 什么时候发货？",
                "timestamp": "2026-07-20T10:00:00Z",
            },
            {
                "id": "msg-2",
                "role": "assistant",
                "content": "您的订单已在配送中，预计今日 18:00 前送达。",
                "timestamp": "2026-07-20T10:00:30Z",
            },
        ],
    }


async def test_e2e_04_evaluation_full_flow(
    agent_client,
    tenant_headers: dict[str, str],
    trace_id: str,
):
    """端到端：保存对话→自动评分→生成报告→查看详情→生成建议。

    验证点：
    - 保存对话后 conversationId 一致
    - 自动评分生成 6 维度分数 + overallScore
    - 生成报告返回 reportId / totalTasks / avgQualityScore / dimensions
    - 报告详情包含 dimensions / suggestions / autoGenerated
    - 生成建议返回 suggestions 列表，每条含 category / priority
    - 全链路 traceId 回传一致
    """

    employee_id = "emp-e2e-04"
    conv_id = "conv-e2e-04-001"

    # 1. 保存对话记录
    save_resp = await agent_client.post(
        f"{EVAL_BASE}/conversations",
        json=_conv_body(conv_id, employee_id),
        headers=tenant_headers,
    )
    assert save_resp.status_code == 200, save_resp.text
    saved = save_resp.json()["data"]
    assert saved["conversationId"] == conv_id
    assert saved["employeeId"] == employee_id
    assert len(saved["messages"]) == 2
    assert save_resp.json()["traceId"] == trace_id

    # 2. 触发自动评分（LLM mode，mock 实现）
    score_resp = await agent_client.post(
        f"{EVAL_BASE}/conversations/{conv_id}/auto-score",
        json={},
        headers=tenant_headers,
    )
    assert score_resp.status_code == 200, score_resp.text
    score_data = score_resp.json()["data"]
    assert score_data["conversationId"] == conv_id
    assert 0 <= score_data["overallScore"] <= 100
    assert len(score_data["dimensions"]) == 6
    assert score_data["mode"] == "llm"
    assert score_data["evaluatorModel"]
    assert score_resp.json()["traceId"] == trace_id

    # 2a. 验证评分确定性（同一对话多次评分结果一致）
    score_resp_2 = await agent_client.post(
        f"{EVAL_BASE}/conversations/{conv_id}/auto-score",
        json={},
        headers=tenant_headers,
    )
    assert score_resp_2.status_code == 200
    assert score_resp_2.json()["data"]["overallScore"] == score_data["overallScore"]

    # 3. 生成评估报告（需先有 scored conversations）
    # 多保存几条对话并评分，让报告有足够数据
    for i in range(2):
        cid = f"conv-e2e-04-{i:02d}"
        await agent_client.post(
            f"{EVAL_BASE}/conversations",
            json=_conv_body(cid, employee_id),
            headers=tenant_headers,
        )
        await agent_client.post(
            f"{EVAL_BASE}/conversations/{cid}/auto-score",
            json={},
            headers=tenant_headers,
        )

    report_resp = await agent_client.post(
        f"{EVAL_BASE}/reports/generate",
        json={"employeeId": employee_id, "period": "2026-W29"},
        headers=tenant_headers,
    )
    assert report_resp.status_code == 200, report_resp.text
    report_data = report_resp.json()["data"]
    assert report_data["employeeId"] == employee_id
    assert report_data["period"] == "2026-W29"
    assert report_data["totalTasks"] >= 3
    assert 0 <= report_data["avgQualityScore"] <= 100
    assert "reportId" in report_data
    assert "comparisonBaseline" in report_data
    assert "dimensions" in report_data
    assert report_data["autoGenerated"] is True
    assert report_resp.json()["traceId"] == trace_id
    report_id = report_data["reportId"]

    # 4. 查看报告详情
    detail_resp = await agent_client.get(
        f"{EVAL_BASE}/reports/{report_id}", headers=tenant_headers
    )
    assert detail_resp.status_code == 200
    detail = detail_resp.json()["data"]
    assert detail["reportId"] == report_id
    assert "dimensions" in detail
    assert "suggestions" in detail
    assert detail["autoGenerated"] is True
    assert detail_resp.json()["traceId"] == trace_id

    # 5. 生成优化建议
    sug_resp = await agent_client.post(
        f"{EVAL_BASE}/suggestions/generate",
        json={"employeeId": employee_id, "period": "30d"},
        headers=tenant_headers,
    )
    assert sug_resp.status_code == 200, sug_resp.text
    sug_data = sug_resp.json()["data"]
    assert "suggestions" in sug_data
    assert "generatedAt" in sug_data
    assert len(sug_data["suggestions"]) > 0
    first_sug = sug_data["suggestions"][0]
    assert "category" in first_sug
    assert "priority" in first_sug
    assert "title" in first_sug
    assert "action" in first_sug
    assert sug_resp.json()["traceId"] == trace_id

    # 6. 查询建议列表
    list_sug_resp = await agent_client.get(
        f"{EVAL_BASE}/suggestions",
        params={"employeeId": employee_id},
        headers=tenant_headers,
    )
    assert list_sug_resp.status_code == 200
    assert len(list_sug_resp.json()["data"]) > 0


async def test_e2e_04_evaluation_manual_score(
    agent_client,
    tenant_headers: dict[str, str],
):
    """人工评分链路：保存对话→人工评分→查看。"""
    conv_id = "conv-e2e-04-manual"
    await agent_client.post(
        f"{EVAL_BASE}/conversations",
        json=_conv_body(conv_id, "emp-manual"),
        headers=tenant_headers,
    )

    resp = await agent_client.post(
        f"{EVAL_BASE}/conversations/{conv_id}/score",
        json={"score": 88.5, "evaluatedBy": "reviewer-zhang"},
        headers=tenant_headers,
    )
    assert resp.status_code == 200, resp.text
    data = resp.json()["data"]
    assert data["overallScore"] == 88.5
    assert data["mode"] == "manual"
    assert data["evaluatorModel"] == "manual"


async def test_e2e_04_evaluation_rubric_flow(
    agent_client,
    tenant_headers: dict[str, str],
):
    """评分规则链路：列出默认 rubric → 保存自定义 rubric → 再次列出。"""
    # 列出默认 rubric
    resp = await agent_client.get(f"{EVAL_BASE}/rubrics", headers=tenant_headers)
    assert resp.status_code == 200
    rubrics = resp.json()["data"]
    assert len(rubrics) >= 1
    default = next(r for r in rubrics if r["id"] == "rubric-default")
    assert len(default["dimensions"]) == 6
    # 权重和应接近 1.0
    total = sum(d["weight"] for d in default["dimensions"])
    assert abs(total - 1.0) < 0.01

    # 保存自定义 rubric
    custom = {
        "id": "rubric-e2e-04",
        "name": "E2E 自定义规则",
        "dimensions": [
            {"dimension": "accuracy", "weight": 0.4, "description": "准确性"},
            {"dimension": "compliance", "weight": 0.6, "description": "合规"},
        ],
    }
    save_resp = await agent_client.post(
        f"{EVAL_BASE}/rubrics", json=custom, headers=tenant_headers
    )
    assert save_resp.status_code == 200
    assert save_resp.json()["data"]["id"] == "rubric-e2e-04"

    # 再次列出应包含两个
    resp2 = await agent_client.get(f"{EVAL_BASE}/rubrics", headers=tenant_headers)
    ids = [r["id"] for r in resp2.json()["data"]]
    assert "rubric-default" in ids
    assert "rubric-e2e-04" in ids
