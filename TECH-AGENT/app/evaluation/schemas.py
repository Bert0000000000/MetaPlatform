"""Pydantic schemas for agent evaluation (V11-04 APP-DW 效果评估后端化).

Aligns with ``APP-DW/src/types/evaluation.ts`` field-by-field so the frontend
can drop all mock fallbacks.
"""

from __future__ import annotations

from datetime import datetime, timezone
from enum import Enum
from typing import List, Optional

from pydantic import BaseModel, Field


# --------------------------------------------------------------- enums


class EvaluationDimension(str, Enum):
    """6 evaluation dimensions used by auto-scoring."""

    ACCURACY = "accuracy"
    HELPFULNESS = "helpfulness"
    COMPLIANCE = "compliance"
    EFFICIENCY = "efficiency"
    TOOL_USAGE = "toolUsage"
    CONTEXT_COHERENCE = "contextCoherence"


class EvaluatorMode(str, Enum):
    """Scoring mode."""

    MANUAL = "manual"
    LLM = "llm"
    HYBRID = "hybrid"


class SuggestionCategory(str, Enum):
    """Optimization suggestion categories."""

    PROMPT = "prompt"
    TOOL = "tool"
    KNOWLEDGE = "knowledge"
    PARAMETER = "parameter"
    WORKFLOW = "workflow"


class SuggestionPriority(str, Enum):
    """Optimization suggestion priorities."""

    HIGH = "high"
    MEDIUM = "medium"
    LOW = "low"


# --------------------------------------------------------------- dimension scoring


class RubricDimension(BaseModel):
    """A weighted dimension inside a scoring rubric."""

    dimension: EvaluationDimension
    weight: float = Field(ge=0.0, le=1.0)
    description: str = ""


class DimensionScore(BaseModel):
    """A single dimension score produced by the evaluator."""

    dimension: EvaluationDimension
    score: float = Field(ge=0.0, le=100.0)
    weight: Optional[float] = 1.0
    reasoning: Optional[str] = None
    evidence: Optional[List[str]] = None


class AutoScoreResult(BaseModel):
    """Auto-score result for a single conversation."""

    conversationId: str
    overallScore: float = Field(ge=0.0, le=100.0)
    dimensions: List[DimensionScore]
    evaluatorModel: str
    evaluatedAt: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    summary: str = ""
    mode: EvaluatorMode = EvaluatorMode.LLM


# --------------------------------------------------------------- scoring rubric


class ScoringRubric(BaseModel):
    """A reusable scoring rubric with weighted dimensions."""

    id: str
    name: str
    dimensions: List[RubricDimension]
    updatedAt: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))


# --------------------------------------------------------------- optimization suggestions


class OptimizationSuggestion(BaseModel):
    """An optimization suggestion produced from evaluation results."""

    id: str
    category: SuggestionCategory
    priority: SuggestionPriority
    title: str
    description: str
    action: str
    expectedImpact: str
    relatedEvidence: Optional[List[str]] = None


class GenerateSuggestionsRequest(BaseModel):
    """Request body for ``POST /suggestions/generate``."""

    employeeId: str = Field(min_length=1)
    period: Optional[str] = None
    reportId: Optional[str] = None


class GenerateSuggestionsResponse(BaseModel):
    """Response body for ``POST /suggestions/generate``."""

    suggestions: List[OptimizationSuggestion]
    generatedAt: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    basedOnReportId: Optional[str] = None


# --------------------------------------------------------------- evaluation reports


class EvaluationReport(BaseModel):
    """Aggregated evaluation report for an employee over a period."""

    reportId: str
    employeeId: str
    period: str
    totalTasks: int = 0
    avgQualityScore: float = 0.0
    successRate: float = 0.0
    avgDuration: float = 0.0
    highlights: List[str] = Field(default_factory=list)
    issues: List[str] = Field(default_factory=list)
    createdAt: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))


class EvaluationReportDetail(EvaluationReport):
    """Extended report with dimension scores and suggestions."""

    dimensions: Optional[List[DimensionScore]] = None
    suggestions: Optional[List[OptimizationSuggestion]] = None
    autoGenerated: Optional[bool] = None
    scoreBreakdown: Optional[dict] = None
    comparisonBaseline: Optional[dict] = None


class GenerateReportRequest(BaseModel):
    """Request body for ``POST /reports/generate``."""

    employeeId: str = Field(min_length=1)
    period: str = Field(min_length=1)


# --------------------------------------------------------------- conversation record


class ConversationMessageRecord(BaseModel):
    """A message inside a ConversationRecord payload."""

    id: str
    role: str
    content: str
    toolCall: Optional[dict] = None
    timestamp: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))


class ConversationRecord(BaseModel):
    """Conversation record used by APP-DW evaluation UI.

    The frontend ``saveConversation`` API posts this body; the backend persists
    it as the source of truth for later scoring/report generation.
    """

    conversationId: str = Field(min_length=1)
    employeeId: str = Field(min_length=1)
    taskId: str = ""
    messages: List[ConversationMessageRecord] = Field(default_factory=list)
    qualityScore: Optional[float] = None
    evaluatedBy: Optional[str] = None
    evaluatedAt: Optional[datetime] = None
    createdAt: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))


class ManualScoreRequest(BaseModel):
    """Request body for ``POST /conversations/{id}/score``."""

    score: float = Field(ge=0.0, le=100.0)
    evaluatedBy: str = Field(min_length=1)


class AutoScoreRequest(BaseModel):
    """Request body for ``POST /conversations/{id}/auto-score``."""

    rubricId: Optional[str] = None


class BatchAutoScoreRequest(BaseModel):
    """Request body for ``POST /conversations/batch-auto-score``."""

    employeeId: str = Field(min_length=1)
    period: Optional[str] = None
    limit: Optional[int] = Field(default=None, ge=1, le=200)


class BatchAutoScoreResponse(BaseModel):
    """Response body for ``POST /conversations/batch-auto-score``."""

    total: int
    scored: int
    results: List[AutoScoreResult]


class QualityTrendPoint(BaseModel):
    """A single point on the quality trend curve."""

    date: str
    score: float


# --------------------------------------------------------------- aggregate report (V11-06)


class AggregateReportRequest(BaseModel):
    """Request body for ``POST /evaluations/aggregate-report`` (V11-06).

    Aggregates evaluation results across multiple employees (the participants
    of a multi-employee collaboration task) into a single rendered report.
    """

    collaborationId: Optional[str] = None
    employeeIds: List[str] = Field(min_length=1)
    period: Optional[str] = None


class AggregateReportResponse(BaseModel):
    """Response body for ``POST /evaluations/aggregate-report``."""

    collaborationId: Optional[str] = None
    employeeIds: List[str] = Field(default_factory=list)
    totalEmployees: int = 0
    totalConversations: int = 0
    avgQualityScore: float = 0.0
    successRate: float = 0.0
    dimensions: List[DimensionScore] = Field(default_factory=list)
    highlights: List[str] = Field(default_factory=list)
    issues: List[str] = Field(default_factory=list)
    report: str = ""
    generatedAt: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))


# --------------------------------------------------------------- serialization helpers


def score_result_to_dict(result: AutoScoreResult) -> dict:
    return {
        "conversationId": result.conversationId,
        "overallScore": result.overallScore,
        "dimensions": [d.model_dump(mode="json") for d in result.dimensions],
        "evaluatorModel": result.evaluatorModel,
        "evaluatedAt": result.evaluatedAt,
        "summary": result.summary,
        "mode": result.mode.value,
    }


def suggestion_to_dict(s: OptimizationSuggestion) -> dict:
    return {
        "id": s.id,
        "category": s.category.value,
        "priority": s.priority.value,
        "title": s.title,
        "description": s.description,
        "action": s.action,
        "expectedImpact": s.expectedImpact,
        "relatedEvidence": s.relatedEvidence,
    }


def rubric_to_dict(r: ScoringRubric) -> dict:
    return {
        "id": r.id,
        "name": r.name,
        "dimensions": [
            {
                "dimension": d.dimension.value,
                "weight": d.weight,
                "description": d.description,
            }
            for d in r.dimensions
        ],
        "updatedAt": r.updatedAt,
    }


def report_to_dict(r: EvaluationReport) -> dict:
    return {
        "reportId": r.reportId,
        "employeeId": r.employeeId,
        "period": r.period,
        "totalTasks": r.totalTasks,
        "avgQualityScore": r.avgQualityScore,
        "successRate": r.successRate,
        "avgDuration": r.avgDuration,
        "highlights": list(r.highlights),
        "issues": list(r.issues),
        "createdAt": r.createdAt,
    }


def report_detail_to_dict(r: EvaluationReportDetail) -> dict:
    base = report_to_dict(r)
    base.update(
        {
            "dimensions": (
                [d.model_dump(mode="json") for d in r.dimensions]
                if r.dimensions
                else None
            ),
            "suggestions": (
                [suggestion_to_dict(s) for s in r.suggestions]
                if r.suggestions
                else None
            ),
            "autoGenerated": r.autoGenerated,
            "scoreBreakdown": r.scoreBreakdown,
            "comparisonBaseline": r.comparisonBaseline,
        }
    )
    return base


def conversation_record_to_dict(r: ConversationRecord) -> dict:
    return {
        "conversationId": r.conversationId,
        "employeeId": r.employeeId,
        "taskId": r.taskId,
        "messages": [
            {
                "id": m.id,
                "role": m.role,
                "content": m.content,
                "toolCall": m.toolCall,
                "timestamp": m.timestamp,
            }
            for m in r.messages
        ],
        "qualityScore": r.qualityScore,
        "evaluatedBy": r.evaluatedBy,
        "evaluatedAt": r.evaluatedAt,
        "createdAt": r.createdAt,
    }
